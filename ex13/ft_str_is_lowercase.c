int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	if (!*str || str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
			return (0);
		else
			i++;
	}
	return (1);
}
